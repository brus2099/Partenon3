    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            this.archivo.borrarArchivo();
            this.archivo.crear(this.modelo.getDatos().get(0));
            boolean primero = true;
            for (fes.aragon.partenon.Libro object : this.modelo.getDatos()) {
                if (primero) {
                    primero = false;
                } else {
                    this.archivo.update(object);
                }
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error en el archivo");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

