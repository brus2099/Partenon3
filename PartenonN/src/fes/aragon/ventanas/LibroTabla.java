    public LibroTabla(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        try {
            initComponents();
            this.setLocationRelativeTo(this);
            this.archivo = new CRUD("Partenon");
            if (!archivo.existeArchivo()) {
                this.jButton1.setEnabled(false);
            } else {
                for (Object tmp : this.archivo.read()) {
                    fes.aragon.partenon.Libro lib = (fes.aragon.partenon.Libro) tmp;
                    this.modelo.getDatos().add(lib);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(parent, "Error en el archivo");
        }

    }

